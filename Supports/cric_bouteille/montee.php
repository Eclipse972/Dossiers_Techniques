<h1>Mont&eacute;e</h1>
<p>L’action manuelle de l'utilisateur sur le levier provoque la montée de la tige de levée.</p>
<h2>1ère PHASE ( le piston de la pompe monte c'est l'aspiration )</h2>
<p>Entre sa position basse et haute, le piston fait varier le volume d'huile dans le corps de pompe. Dans cette phase ce volume est en expansion. 
L'huile contenu dans le réservoir est aspiré dans la pompe.</p>
<h2>2ème PHASE ( le piston de la pompe descend c'est le refoulement )</h2>
<p>Entre sa position haute  et basse, le piston fait varier le volume d'huile dans le corps de pompe. Dans cette phase ce volume est en diminution. 
L'huile contenu dans la pompe est poussé vers le vérin de levée. La tige de vérin monte et lève la charge.</p>