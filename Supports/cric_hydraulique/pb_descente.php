<h1>Si le cric ne s'abaisse pas totalement après avoir été utilisé :</h1>
<ul>
<li>Il se peut que le ressort de rappel du bras de levage soit cassé ou décroché. Remplacez-le.</li>
<li>Le cric a probablement besoin d’être lubrifié. Nettoyez et graissez toutes les parties mobiles du mécanisme du bras de levage.</li>
<li>Le module hydraulique contient peut-être trop d'huile, vérifiez le niveau d'huile.</li>
</ul>